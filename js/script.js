function gridBox (icons) {
    return `
    <div class="box-icons">
        <i class="${icons.color} ${icons.family} ${icons.prefix}${icons.name}"></i>
        <p class="">${icons.name}</p>
    </div>
    `
}

const containerHtml = document.getElementById('container-icons')

function printIcons(container, icons) {

	container.innerHTML = '';

	icons.forEach((icon) => {

    containerHtml.innerHTML += gridBox(icon)

})
}

printIcons(containerHtml,icons)

const selectIcons = document.getElementById('icons-select');

selectIcons.addEventListener('change', function() {

	const filterIcons = icons.filter ((icon) => {
		return this.value === 'all' || icon.type === this.value
	})
	printIcons(containerHtml,filterIcons)

})